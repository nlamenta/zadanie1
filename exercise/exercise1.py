import math

#pole kwadratu
bok_a = 10.5

pole_kw = bok_a * bok_a

print("Pole kwadratu wynosi: " + str(pole_kw))

#pole koła

r_promien = 3

pole_kola =  math.pi* r_promien ** 2

print("Pole koła wynosi: " + str(pole_kola))

#pole rombu

bok_b = 4

wysokosc_h = 5

pole_rombu = bok_b * wysokosc_h

print("Pole koła wynosi: " + str(pole_rombu))

#pole trpezu

bok_c, bok_d, wysokosc_g = 2, 3, 4

pole_trapezu = ((bok_c + bok_d) * wysokosc_g)/2

print("Pole trapezu wynosi: " + str(pole_trapezu))